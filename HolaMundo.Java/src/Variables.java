public class Variables {
    public static void main(String[] args) {
        //Declarando Variable
        int speed;

        //Asignar valor a una Variable
        speed = 10;
        System.out.println(speed);

        int salary = 20000;
        System.out.println(salary);

        String employeeName = "Cristo";
        System.out.println(employeeName);

    }
}
